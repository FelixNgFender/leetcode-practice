from collections import defaultdict


def isValidSudoku(board):
    rows = defaultdict(set)
    cols = defaultdict(set)
    squares = defaultdict(set)  # key = (r // 3, c // 3)

    for r in range(9):
        for c in range(9):
            element = board[r][c]
            if element == ".":
                continue
            if (
                element in rows[r]
                or element in cols[c]
                or element in squares[(r // 3, c // 3)]
            ):
                return False
            rows[r].add(element)
            cols[c].add(element)
            squares[(r // 3, c // 3)].add(element)
    return True


